package cadastro;

public class Eventos {
	
	public Eventos(String nomeEvento, int data, String nomeResponsavel, int matriculaResponsavel, String setorReservado,
			int quantidadeVagas) {
		super();
		this.nomeEvento = nomeEvento;
		this.data = data;
		this.nomeResponsavel = nomeResponsavel;
		this.matriculaResponsavel = matriculaResponsavel;
		this.setorReservado = setorReservado;
		this.quantidadeVagas = quantidadeVagas;
	}
	public String nomeEvento;
	public int data;
	public String nomeResponsavel;
	public int matriculaResponsavel;
	public String setorReservado;
	public int quantidadeVagas;
	
	
	
}
